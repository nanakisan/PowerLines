package untouchedwagons.minecraft.powerlines.extra;

public class ConnectionPointCoordinate {
    private final float x,z;

    public ConnectionPointCoordinate(float x, float z) {
        this.x = x;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getZ() {
        return z;
    }
}
